    private void grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grabarActionPerformed
        String info="Nombre/s y apellido/s: ";
        
        JTextField[]na={nombre};
        for(int i=0;i<na.length;i++){
            info+=na[i].getText()+" ";
        }
        
        info+="\nCorreo: ";
        JTextField[]cu={correo};
        for(int i=0;i<cu.length;i++){
            info+=cu[i].getText()+" ";
        }
        
        info+="\nHorario de Atencion: "+horario.getSelectedItem().toString();
        
        JOptionPane.showMessageDialog(this,info,"Muestra de datos",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_grabarActionPerformed

